// int xkbhit (void)       
// 컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴
// Arguments:
// Return:    True if a key is waiting, False otherwise
// 컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴
// Checks the keyboard buffer to determine whether or not a key is waiting
// to be received.
// 컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴
int xkbhit (void)
{
int ret;

   disable();
   ret = Head - Tail;
   enable();

   return ret;
}

