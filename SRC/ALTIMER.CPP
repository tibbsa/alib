// Initializes a timer
// 컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴
void Timer::set (time_t length)
{
   Alert (length == 0L);
   t_Set = time (NULL);
   t_Expires = t_Set + length;
}

